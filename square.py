while True:
    # Запрашиваем длины сторон прямоугольника
    a = float(input("Введите длину первой стороны: a= "))
    b = float(input("Введите длину второй стороны: b= "))

    # Проверяем, что стороны больше нуля
    if a > 0 and b > 0:
        # Вычисляем площадь прямоугольника
        s = a * b
        print(f"Площадь прямоугольника со сторонами a={a} и b={b} равна s = a * b = {s}")
        break  # Выход из цикла при корректных значениях
    else:
        # Выводим сообщение об ошибке и возвращаемся на повторный ввод
        print("Ошибка: длины сторон прямоугольника должны быть больше нуля. Попробуйте еще раз.")
